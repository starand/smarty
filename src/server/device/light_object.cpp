#include <common/StdAfx.h>

#include <device/light_object.h>


//--------------------------------------------------------------------------------------------------

light_object_t::light_object_t( )
    : m_turnoff_time( 0 )
{
}

//--------------------------------------------------------------------------------------------------

void light_object_t::set_turnoff_time( time_t time )
{
    m_turnoff_time = time;
}

//--------------------------------------------------------------------------------------------------

void light_object_t::clear_turnoff_time( )
{
    m_turnoff_time = 0;
}

//--------------------------------------------------------------------------------------------------

time_t light_object_t::get_turnoff_timeout( )
{
    return m_turnoff_time;
}

